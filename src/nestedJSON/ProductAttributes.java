package nestedJSON;

public class ProductAttributes {
	
	private String color;
	private String size;
	private String type;
	private double ounces;
	
	public String getColor() {
		return color;
	}
	public String getSize() {
		return size;
	}
	public String getType() {
		return type;
	}
	public double getOunces() {
		return ounces;
	}
	
	
	

}
